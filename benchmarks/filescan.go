package benchmarks

import (
	"bufio"
	"os"
)

type fileScan struct {
	filename string
}

func (fileScan *fileScan) Id() string {
	return "filescan: " + fileScan.filename
}

func (fileScan *fileScan) Load(inputfile string, testdir string) error {
	fileScan.filename = inputfile
	return nil
}

func (fileScan *fileScan) Test(forMatch string) (bool, error) {
	data, err := os.Open(fileScan.filename)
	if err != nil {
		return false, err
	}
	defer data.Close()

	rootdomain := rootdomain(forMatch)

	scanner := bufio.NewScanner(data)
	for scanner.Scan() {
		text := scanner.Text()
		if text == forMatch || text == rootdomain {
			return true, nil
		}
	}
	return false, nil
}

func (fileScan *fileScan) Teardown() error {
	return nil
}